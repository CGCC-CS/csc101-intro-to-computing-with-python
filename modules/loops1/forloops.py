# Looping Through Data
# Ranges
for i in range(1, 11):
    print(i)  # 1 to 10
    
# Strings
for ch in "Python":
    print(ch)

# Lists
for num in [2, 4, 6, 8]:
    print(num)

# Tuples
for coord in (1, 2, 3):
    print(coord)
    
# Dictionaries
student_grades = {"Alice": 85, "Bob": 92, "Charlie": 78}
for name in student_grades:
    print(f"{name}: {student_grades[name]}")
        