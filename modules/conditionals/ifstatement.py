# Basic if Statement and Comparison Operators

print("=== Example 1: Basic Conditionals ===")

age = int(input("Enter your age: "))

if age >= 18:
    print("You are an adult!")

if age < 13:
    print("You are a child!")

# Multiple conditions
score = 85
if score >= 90:
    print("Excellent!")
if score == 85:
    print("Good job!")
if score != 100:
    print("There's room for improvement!")

print()
